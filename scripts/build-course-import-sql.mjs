/* global console, process */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

function normalizeText(value) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();
}

function parseBoolean(value) {
  const normalized = normalizeText(value ?? "");
  return normalized === "sim" || normalized === "s";
}

function parseDurationSemesters(value) {
  const match = String(value ?? "").match(/\d+/);
  return match ? Number(match[0]) : 0;
}

function escapeSql(value) {
  return String(value ?? "").replace(/'/g, "''");
}

function parseCsv(content) {
  const lines = content
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean);

  const [, ...rows] = lines;

  return rows.map((row) => {
    const [
      code,
      name,
      type,
      duration,
      presential,
      semipresential,
      ead,
      matutino,
      vespertino,
      noturno,
      flexible
    ] = row.split(";");

    return {
      code: code.trim(),
      name: name.trim(),
      type: type.trim(),
      durationSemesters: parseDurationSemesters(duration),
      hasPresential: parseBoolean(presential),
      hasSemipresential: parseBoolean(semipresential),
      hasEad: parseBoolean(ead),
      hasMatutino: parseBoolean(matutino),
      hasVespertino: parseBoolean(vespertino),
      hasNoturno: parseBoolean(noturno),
      hasFlexibleHours: parseBoolean(flexible)
    };
  });
}

function buildCourseUpsert(course) {
  const observations = course.hasEad
    ? "'Oferta EAD padrao com mensalidade e matricula de R$ 109,00.'"
    : "NULL";

  return `
INSERT INTO "Course" (
  "id",
  "code",
  "name",
  "type",
  "durationSemesters",
  "hasPresential",
  "hasSemipresential",
  "hasEad",
  "hasMatutino",
  "hasVespertino",
  "hasNoturno",
  "hasFlexibleHours",
  "autoOfferMode",
  "observations",
  "createdAt",
  "updatedAt"
)
VALUES (
  'course_${escapeSql(course.code.toLowerCase())}',
  '${escapeSql(course.code)}',
  '${escapeSql(course.name)}',
  '${escapeSql(course.type)}',
  ${course.durationSemesters},
  ${course.hasPresential},
  ${course.hasSemipresential},
  ${course.hasEad},
  ${course.hasMatutino},
  ${course.hasVespertino},
  ${course.hasNoturno},
  ${course.hasFlexibleHours},
  ${course.hasEad},
  ${observations},
  NOW(),
  NOW()
)
ON CONFLICT ("code") DO UPDATE SET
  "name" = EXCLUDED."name",
  "type" = EXCLUDED."type",
  "durationSemesters" = EXCLUDED."durationSemesters",
  "hasPresential" = EXCLUDED."hasPresential",
  "hasSemipresential" = EXCLUDED."hasSemipresential",
  "hasEad" = EXCLUDED."hasEad",
  "hasMatutino" = EXCLUDED."hasMatutino",
  "hasVespertino" = EXCLUDED."hasVespertino",
  "hasNoturno" = EXCLUDED."hasNoturno",
  "hasFlexibleHours" = EXCLUDED."hasFlexibleHours",
  "autoOfferMode" = EXCLUDED."autoOfferMode",
  "observations" = EXCLUDED."observations",
  "updatedAt" = NOW();`;
}

function buildOfferStatement(course) {
  if (!course.hasEad) {
    return `
UPDATE "Offer"
SET "active" = false, "updatedAt" = NOW()
WHERE "courseId" = (
  SELECT "id" FROM "Course" WHERE "code" = '${escapeSql(course.code)}'
);`;
  }

  return `
UPDATE "Offer"
SET "active" = false, "updatedAt" = NOW()
WHERE "courseId" = (
  SELECT "id" FROM "Course" WHERE "code" = '${escapeSql(course.code)}'
)
AND "id" <> 'offer_${escapeSql(course.code.toLowerCase())}_ead';

INSERT INTO "Offer" (
  "id",
  "courseId",
  "monthlyPrice",
  "enrollmentFee",
  "firstMonthlyDueLabel",
  "durationLabel",
  "notes",
  "active",
  "createdAt",
  "updatedAt"
)
VALUES (
  'offer_${escapeSql(course.code.toLowerCase())}_ead',
  (SELECT "id" FROM "Course" WHERE "code" = '${escapeSql(course.code)}'),
  109.00,
  109.00,
  'Agosto',
  '${escapeSql(`${course.durationSemesters} semestres`)}',
  'Oferta EAD padrao fixa de R$ 109,00 para mensalidade e matricula.',
  true,
  NOW(),
  NOW()
)
ON CONFLICT ("id") DO UPDATE SET
  "courseId" = EXCLUDED."courseId",
  "monthlyPrice" = EXCLUDED."monthlyPrice",
  "enrollmentFee" = EXCLUDED."enrollmentFee",
  "firstMonthlyDueLabel" = EXCLUDED."firstMonthlyDueLabel",
  "durationLabel" = EXCLUDED."durationLabel",
  "notes" = EXCLUDED."notes",
  "active" = true,
  "updatedAt" = NOW();`;
}

function main() {
  const csvPath = resolve(process.cwd(), "prisma/data/CURSOS.csv");
  const outputPath = resolve(process.cwd(), "prisma/data/import_courses.sql");
  const content = readFileSync(csvPath).toString("latin1");
  const courses = parseCsv(content);

  const statements = [
    "-- generated by scripts/build-course-import-sql.mjs",
    'DROP INDEX IF EXISTS "Course_name_key";',
    'DROP INDEX IF EXISTS "Course_name_type_key";'
  ];

  for (const course of courses) {
    statements.push(buildCourseUpsert(course));
    statements.push(buildOfferStatement(course));
  }

  writeFileSync(outputPath, `${statements.join("\n\n")}\n`);

  console.log(
    JSON.stringify(
      {
        generated: outputPath,
        importedRows: courses.length
      },
      null,
      2
    )
  );
}

main();
